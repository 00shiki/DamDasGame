export class Soal {
  soal: String
  a1: String
  a2: String
  a3: String
  a4: String
  ans: String

  constructor(
    soal: String,
    a1: String,
    a2: String,
    a3: String,
    a4: String,
    ans: String
  ) {
    this.soal = soal
    this.a1 = a1
    this.a2 = a2
    this.a3 = a3
    this.a4 = a4
    this.ans = ans
  }
}

function getMultipleRandom(arr: Array<Soal>, num: number) {
  const shuffled = [...arr].sort(() => 0.5 - Math.random())

  return shuffled.slice(0, num)
}

